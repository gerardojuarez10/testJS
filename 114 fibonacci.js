function fibonacci(num){
    if (num == 0){
        return 0;
    } if (num == 1){
        return 0 + 1;
    } else { 
        //2             (2-1=1) = 0+1 =1  (2-2=0) = 0  => 1
        //3             (3-1=2)= 1            (3-2=1)= 1  => 2
        //4             (3)=2               (2)=1         =>3
        //5             (4)=3              (3) = 2        => 5
        //6              (5)=5            (4)=3           => 8
        //7             (6)=8             (5)=5           => 13
        //8             (7) =13            (6) = 8         => 21
        //9             (8)= 21            (7)= 13          => 34
        //10            (9)=34             (8)=21           =>55
        //11            (10)=55            (9) = 34         => 89
        //12            (11)=89            (10)=55           => 144
        //13            (12)=144            (11)=89           => 233
        //14            (13)=233            (12)=144           => 377
        //15            (14)=377            (13)=233           => 610
        return fibonacci(num-1)+ fibonacci(num-2);
    }
}
